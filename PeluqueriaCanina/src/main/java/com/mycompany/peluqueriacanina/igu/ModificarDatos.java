    private void cargarDatos(long numCliente) {
        
        //llamo a la mascota que tenga ese numero de cliente y guardo en un objeto de tipo mascota
        this.masco = controlLogica.traerMascota(numCliente);
        
        //seteo todos los datos en la igu para que la persona pueda verlos
        txtNombre.setText(masco.getNombre());
        txtRaza.setText(masco.getRaza());
        txtCelDuenio.setText(masco.getUnDuenio().getCelDuenio());
        txtColor.setText(masco.getColor());
        txtNomDueño.setText(masco.getUnDuenio().getNombre());
        txtObservasiones.setText(masco.getObservaciones());
        
        if(masco.getAlergico().equals("SI")){
            cmbAlergico.setSelectedIndex(1);
        }else{
            if(masco.getAlergico().equals("NO")){
                cmbAlergico.setSelectedIndex(2);
            }
        }
        
        
        if(masco.getAtencionEspecial().equals("SI")){
            cmbAtencionEspecial.setSelectedIndex(1);
        }else{
            if(masco.getAtencionEspecial().equals("NO")){
                cmbAtencionEspecial.setSelectedIndex(2);
            }
        }
    }
